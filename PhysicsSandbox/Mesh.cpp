	Mesh::Mesh(float const* verts, size_t verts_sz, unsigned const* idxs, size_t idxs_sz)
		: m_verts_sz{verts_sz},
		m_idxs_sz{idxs_sz},
		m_vao{0}, m_vbo{0}, m_ebo{0}
	{
		glGenVertexArrays(1, &m_vao);
		glBindVertexArray(m_vao);

		// Vertex Buffer
		glGenBuffers(1, &m_vbo);
		glBindBuffer(GL_ARRAY_BUFFER, m_vbo);
		glBufferData(GL_ARRAY_BUFFER, m_verts_sz * sizeof(float), verts, GL_STATIC_DRAW);
		glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)0);
		glEnableVertexAttribArray(0);
		glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(3 * sizeof(float)));
		glEnableVertexAttribArray(1);
		glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(6 * sizeof(float)));
		glEnableVertexAttribArray(2);

		// Element Buffer
		glGenBuffers(1, &m_ebo);
		glBindBuffer(GL_ELEMENT_ARRAY_BUFFER, m_ebo);
		glBufferData(GL_ELEMENT_ARRAY_BUFFER, idxs_sz * sizeof(unsigned), idxs, GL_STATIC_DRAW);

		glBindBuffer(GL_ARRAY_BUFFER, 0);
		glBindVertexArray(0);
	}

	Mesh::Mesh(std::span<float> verts, std::span<unsigned> idxs)
		: Mesh(verts.data(), verts.size(), idxs.data(), idxs.size())
	{ }

	Mesh::Mesh(Mesh&& src) noexcept
		: m_verts_sz{ src.m_verts_sz },
		m_idxs_sz{ src.m_idxs_sz },
		m_vao{ src.m_vao },
		m_vbo{ src.m_vbo },
		m_ebo{ src.m_ebo }
	{
		// Invalidate GL buffers
		src.m_vao = 0;
		src.m_vbo = 0;
		src.m_ebo = 0;
	}

	Mesh& Mesh::operator=(Mesh&& src) noexcept 
	{
		if (&src == this) {	return *this; }

		// values of 0 are silently ignored
		glDeleteBuffers(1, &m_ebo);
		glDeleteBuffers(1, &m_vbo);
		glDeleteVertexArrays(1, &m_vao);

		m_verts_sz = src.m_verts_sz;
		m_idxs_sz = src.m_idxs_sz;
		m_vao = src.m_vao;
		m_vbo = src.m_vbo;
		m_ebo = src.m_ebo;

		src.m_vao = 0;
		src.m_vbo = 0;
		src.m_ebo = 0;

		return *this;
	}


	Mesh::~Mesh() noexcept 
	{
		// values of 0 are silently ignored
		glDeleteBuffers(1, &m_ebo);
		glDeleteBuffers(1, &m_vbo);
		glDeleteVertexArrays(1, &m_vao);
	}

	void Mesh::Use() const noexcept
	{
		glBindVertexArray(m_vao);
	}

	void Mesh::Unuse() const noexcept
	{
		glBindVertexArray(0);
	}

	void Mesh::Draw() const noexcept
	{
		glDrawElements(GL_TRIANGLES, (GLsizei)m_idxs_sz, GL_UNSIGNED_INT, 0);
	}


